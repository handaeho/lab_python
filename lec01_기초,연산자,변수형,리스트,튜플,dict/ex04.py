# 19.11.04 Python Class 04
"""
연산자(Operator)
- 할당 연산자 : =
- 산술 연산자 : +, -, *, **, /, //, %
- 복합 할당 연산자 : +=, -=, *=, /=, ...
- 비교 연산자 : >, >=, <, <=, ==, !=
- 논리 연산자 : and, or, not
- Identity 연산 : is, is not (id() 함수의 리턴 값이 같은지 다른지 비교)
"""
# -----------------------------------------------------------------------------
a = 3
print(a)
# -----------------------------------------------------------------------------
print(2 ** 3)  # 2 x 2 x 2
print(10 / 3)  # 몫과 나머지 출력
print(10 // 3)  # 몫만 출력
print(10 % 3)  # 나머지만 출력
# -----------------------------------------------------------------------------
x = 1
print('x = ', x)
# 결과 x = 1

x += 10
print('x = ', x)
# 결과 x =  11

x -= 8
print('x = ', x)
# 결과 x =  3
# -----------------------------------------------------------------------------
print(1 == 2)
# 결과 False
print(1 != 2)
# 결과 True
# -----------------------------------------------------------------------------
z = 50
print(0 < z and z < 100)
# 결과 True
print(x < 0 or x > 10)
# 결과 False
# -----------------------------------------------------------------------------
