# 변수 사용 : 프로그램에 필요한 데이터를 저장하는 공간
# 변수 이름 = 값
age = 16
print(age)

name = '대호'
print(name)

str1 = 'He said "yes"'
print(str1)

# 여러가지 print 방법
print('나이 = ', age, '이름 = ', name)
print(f'나이:{age}, 이름: {name}') # formated string
print('나이: {}, 이름: {}'.format(age, name))
print('나이: %d, 이름: %s' % (age, name)) # %d - 정수 / %s - 문자열 / %f - 실수









